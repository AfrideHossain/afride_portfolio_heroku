        /* ============= Slider section Start ============ */
        var slide_index = 1;
        show_slide(slide_index);
        function change_slide(n) {
            show_slide(slide_index = n);
        }
        function show_slide(e) {
            let i;
            const slides = document.getElementsByClassName("slide");
            const dot_btn = document.getElementsByClassName("dot_btn");
            for (i = 0; i < slides.length; i++) {
                slides[i].style.display = "none";
                slides[i].classList.remove("fade");
                dot_btn[i].classList.remove("active_circle");
            }
            slides[slide_index -1].style.display = "flex";
            slides[slide_index -1].classList.add("fade");
            dot_btn[slide_index -1].classList.add("active_circle");
            //setTimeout(show_slide, 2000);
        }
        setTimeout(show_slide, 2000);
